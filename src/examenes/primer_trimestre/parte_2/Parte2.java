    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        StringBuilder sb = new StringBuilder("Modo edicion ");
        if (modoEdicion) {
            sb.append("activado").append("\n");
        } else {
            sb.append("desactivado").append("\n");
        }
        sb.append("Equipo ");
        if (rdBtnCelta.isSelected()) {
            sb.append("Celta").append("\n");
        } else {
            sb.append("Deportivo").append("\n");
        }
        sb.append("Estado Civil:  ");
        if (rdBtnSoltero.isSelected()) {
            sb.append("Soltero").append("\n");
        } else {
            sb.append("No Soltero").append("\n");
        }
        if (chkIngles.isSelected()) {
            sb.append(chkIngles.getText()).append("\n");
        }
        if (chkFrances.isSelected()) {
            sb.append(chkFrances.getText()).append("\n");
        }
        if (chkAleman.isSelected()) {
            sb.append(chkAleman.getText()).append("\n");
        }
        sb.append("Edad ").append(jSlider1.getValue()).append(" años");
        sb.append("\n");

        if (jList1.getSelectedIndices().length >= 1) {
            ArrayList<String> list = (ArrayList<String>) jList1.getSelectedValuesList();
            int[] valores = jList1.getSelectedIndices();
            for (int i = 0; i < valores.length; i++) {
                sb.append("Provincia: ").append(list.get(i));
                sb.append(" posicion: ").append(valores[i]).append("\n");
            }
        } else {
            sb.append("Ninguna provincia seleccionada");

        }
        jTextArea1.setText(sb.toString());
    }//GEN-LAST:event_btnAceptarActionPerformed

